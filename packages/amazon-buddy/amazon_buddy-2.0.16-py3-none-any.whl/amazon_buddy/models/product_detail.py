# --------------------------------------------------------- class: ProductDetail --------------------------------------------------------- #

class ProductDetail(JSONCodable):

    # ------------------------------------------------------------- Init ------------------------------------------------------------- #

    def __init__(
        self,
        name: str,
        description: str
    ):
        self.name = name
        self.description = description



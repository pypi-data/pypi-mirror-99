# Amounts of bytes (base 10).
ONE_KB = 1000
ONE_MB = 1000 * ONE_KB
ONE_GB = 1000 * ONE_MB
ONE_TB = 1000 * ONE_GB


# Amounts of seconds.
ONE_MINUTE = 60
ONE_HOUR = 60 * ONE_MINUTE
ONE_DAY = 24 * ONE_HOUR
ONE_WEEK = 7 * ONE_DAY

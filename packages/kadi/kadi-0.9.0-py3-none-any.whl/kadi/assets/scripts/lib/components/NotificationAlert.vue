<template>
  <div class="mb-4" v-if="active">
    <div class="alert fade show" :class="'alert-' + type">
      <i class="fa" :class="'fa-' + icon" v-if="icon"></i> {{ message }}
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      icon: null,
      active: true,
    };
  },
  props: {
    message: String,
    type: {
      type: String,
      default: 'info',
    },
    timeout: {
      type: Number,
      default: 5000,
    },
  },
  mounted() {
    if (this.type === 'info') {
      this.icon = 'exclamation-circle';
    } else if (this.type === 'danger') {
      this.icon = 'times-circle';
    } else if (this.type === 'warning') {
      this.icon = 'exclamation-triangle';
    } else if (this.type === 'success') {
      this.icon = 'check-circle';
    }

    if (this.timeout !== null) {
      setTimeout(() => this.active = false, this.timeout);
    }
  },
};
</script>

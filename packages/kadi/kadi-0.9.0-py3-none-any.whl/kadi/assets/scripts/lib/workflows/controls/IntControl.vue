<template>
  <input class="form-control d-inline" v-model.number="value" @pointerdown.stop>
</template>

<script>
import {InputControlMixin} from 'core';

export default {
  mixins: [InputControlMixin],
  props: {
    defaultValue: Number,
  },
  methods: {
    validateValue() {
      if (isNaN(this.value)) {
        this.value = this.defaultValue;
      } else {
        this.value = Math.floor(this.value);
      }
    },
  },
};
</script>

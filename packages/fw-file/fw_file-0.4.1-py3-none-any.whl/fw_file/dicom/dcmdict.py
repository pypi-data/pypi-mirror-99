# See: https://github.com/DCMTK/dcmtk/blob/DCMTK-3.6.6/dcmdata/data/private.dic
DCMTK_LINE_RE = (
    r"^\("
    r"(?P<group>[0-9a-f]{4})(-o-(?P<group_end>[0-9a-f]{4}))?,"
    r'"(?P<creator>[^"]+)",'
    r"(?P<elem>[0-9a-f]{2,4})"
    r"\)"
    r"\t(?P<VR>[A-Z]{2})"
    r"\t(?P<name>[^\t]+)"
    r"\t(?P<VM>[^\t]+)"
)


def load_dcmdict(file: AnyFile) -> None:
    """Parse and load DCMTK-style private dictionary file.

    Extend pydicom's private dictionaries.
    """
    with BinFile(file) as file_obj:
        for line in file_obj:
            match = re.match(DCMTK_LINE_RE, line)
            if not match:
                continue

            group = int(match.group("group"), 16)
            group_end = int(match.group("group_end") or "0", 16) or group
            creator = match.group("creator")
            elem = int(match.group("elem"), 16)
            VR = match.group("VR")  # pylint: disable=invalid-name
            name = match.group("name")
            VM = match.group("VM")  # pylint: disable=invalid-name

            for grp in range(group, group_end + 1):
                if grp % 2 == 0:
                    continue
                tag = grp << 16 | elem
                add_private_dict_entry(creator, tag, VR, name, VM)


def load_private_dictionaries() -> None:
    """Load our private dictionaries and DCMDICTPATH if specified."""
    for creator, entries in private_dict.items():
        pydicom_private_dict.setdefault(creator, {}).update(entries)

    # DCMDICTPATH has the highest priority
    dcm_dict_path = os.environ.get("DCMDICTPATH")
    if dcm_dict_path:
        load_dcmdict(dcm_dict_path)  # pragma: no cover


